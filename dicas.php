                        <p><strong>1- Use as oportunidades a seu favor</strong></p>
                        <p>- O final de ano é uma época em que costumamos encontrar amigos e familiares que não vemos sempre.</p>
                        <p>Que tal utilizar aquelas perguntas constrangedoras -"o que você tem feito?", "como foi seu ano?" - a seu favor? Fale sobre seus objetivos e planos para o próximo ano.</p>
                        <p><strong>2- Crie uma lista de empresas dos sonhos.</strong></p>
                        <p>Faça uma lista com as empresas em que deseja trabalhar. Procure-as no Linkedln e procure empresas relacionadas e parecidas com elas.</p>
                        <p>Isso pode te ajudar a ter mais foco e tornar a tarefa mais empolgante.</p>
                        <p><strong>3- Crie uma lista com objetivos</strong></p>
                        <p>Colocar no papel os seus objetivos para 2015 pode te ajudar a não perder o foco. Pequenas tarefas como "mandar o currículo para três empresas por semana" ou ir "a cinco eventos por mês" podem parecer pequenos objetivos, mas ajudam a conseguir bons resultados.</p>
                        <p><strong>4- Faça almoços mais longos</strong></p>
                        <p>Dezembro é um mês de confraternizações. Faça delas uma oportunidade. Aproveite para marcar almoços e cafés com amigos e outros profissionais. É um bom momento para conseguir informações e fazer novos contatos.</p>
                        <p><strong>5- Procure grupos profissionais na sua área</strong></p>
                        <p>Toda área profissional possui clubes ou associações que costumam fazer eventos durante o final de ano. Aproveite a oportunidade para aumentar sua rede de contatos.</p>
                        <p><strong>6- Mande recados de fim de ano</strong></p>
                        <p>Pode parecer besteira, mas mandar e-mails, presentes, correspondências no fim de ano é uma boa oportunidade para reativar contatos profissionais.</p>
                        <p>Mas nada de correntes malas, hein? Dedique um tempo e escreva mensagens personalizadas e para contatos relevantes.</p>
                        <p><strong>7- Agradeça</strong></p>
                        <p>Em algum momento, você foi ajudado ou teve uma oportunidade dada por alguém. Que tal relembrar as pessoas que ajudaram a sua carrreira e mandar uma mensagem de agradecimento?</p>
                        <p><strong>8- Atualize o seu&nbsp;currículo&nbsp;(enquanto assiste TV)</strong></p>
                        <p>Nós sabemos, atualizar o C.V não é a tarefa mais excitante. Que tal fazer isso enquanto assiste TV? <a href="https://catracalivre.com.br/geral/emprego-trabalho/indicacao/27-modelos-de-curriculo-para-voce-se-inspirar/" target="_blank">Aproveite e leia a matéria do Catraca Livre com sugestões inovadoras para o seu portfólio.</a></p>
                        <p><strong>9- Pratique o seu texto</strong></p>
                        <p>Escrever e-mails, cartas de apresentação e resumos parece tarefa fácil, mas não é. Para evitar aquele bloqueio crie modelos pré-estabelecidos de texto. Assim, você precisará de apenas pequenas atualizações e modificações quando precisar enviá-los para determinado projeto ou empresa.</p>
                        <p><strong>10-&nbsp; Use o Twitter</strong></p>
                        <p>Crie uma conta no Twitter e procure por perfis de recrutadores ou pessoas que trabalham em empresas de seu interesse. Eles costumam utilizar as redes sociais para divulgar vagas.</p>
                        <p><strong>11- Marque um encontro</strong></p>
                        <p>Pode ter certeza que você não é a única pessoa que está querendo mudar de emprego em janeiro. Que tal marcar um encontrou ou uma festa para reunir amigos e contatos profissionais? É uma boa maneira de estabelecer novos planos.</p>
                        <p><strong>12- Faça um site pessoal</strong></p>
                        <p>A melhor maneira de reunir o trabalho que você já realizou, seus projetos e pensamentos é criar um site. <a href="https://catracalivre.com.br/geral/negocio-urbanidade/indicacao/wix-permite-a-criacao-e-edicao-de-sites-gratuitamente/" target="_blank">Veja algumas dicas para criar um gratuitamente.</a></p>
                        <p><strong>13- Escreva artigos</strong></p>
                        <p>Publicar textos sobre a sua área profissional é uma boa maneira de ficar conhecido. Faça uma lista de textos que poderia que poderia escrever e possíveis blogs ou sites para ele.</p>
                        <p><strong>14- Compartilhe algo</strong></p>
                        <p>Reúne artigos, matérias, frases que te inspiram e compartilhe nas redes sociais. É uma boa maneira de mostrar o qual apaixonado pelo o que faz você é.</p>
                        <p><strong>15- Seja voluntário</strong></p>
                        <p>Aproveite o final de ano para dedicar alguns dias ou horas para o trabalho voluntário. Além de ajudar, é uma boa oportunidade para melhorar seu curriculum conhecer novas pessoas.</p>
